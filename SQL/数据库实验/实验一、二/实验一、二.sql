CREATE DATABASE XSCJ;
DROP DATABASE XSCJ;
USE XSCJ;
CREATE Table XSQK(
    学号 CHAR(6) PRIMARY KEY NOT NULL,
    姓名 CHAR(8) NOT NULL,
    性别 char(1) NOT NULL DEFAULT '男',
    出生日期 DATE NOT NULL,
    所在系 VARCHAR(30) DEFAULT '电子信息与人工智能学院',
    联系电话 CHAR(11) NOT NULL DEFAULT '13345678901',
    专业名 CHAR(10) NOT NULL,
    总学分 TINYINT,
    备注 VARCHAR(30)
)

drop table XSQK;

CREATE Table KC(
    序号 INT DEFAULT 0,
    课程号 CHAR(3) NOT NULL PRIMARY KEY,
    课程名 CHAR(20) NOT NULL,
    授课教师 CHAR(4) DEFAULT '默认教师',
    开课学期 TINYINT,
    学时 TINYINT,
    学分 TINYINT
)

DROP TABLE KC;

CREATE Table XS_KC(
    学号 CHAR(6) NOT NULL,
    课程号 CHAR(3) NOT NULL,
    成绩 TINYINT,
    学分 TINYINT DEFAULT 4,
    PRIMARY KEY(学号, 课程号),
    FOREIGN KEY(学号) REFERENCES XSQK(学号),
    FOREIGN KEY(课程号) REFERENCES KC(课程号)
)

DROP TABLE XS_KC;

